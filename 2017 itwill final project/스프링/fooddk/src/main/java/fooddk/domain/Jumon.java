package fooddk.domain;

public class Jumon {
	
	private int j_no;
	private int seat_no;
	private int i_no;
	
	public Jumon() {
	}

	public Jumon(int j_no, int seat_no, int i_no) {
		super();
		this.j_no = j_no;
		this.seat_no = seat_no;
		this.i_no = i_no;
	}
	
	public int getJ_no() {
		return j_no;
	}

	public void setJ_no(int j_no) {
		this.j_no = j_no;
	}

	public int getSeat_no() {
		return seat_no;
	}

	public void setSeat_no(int seat_no) {
		this.seat_no = seat_no;
	}

	public int getI_no() {
		return i_no;
	}

	public void setI_no(int i_no) {
		this.i_no = i_no;
	}

}
